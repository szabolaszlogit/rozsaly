5. A sütik beállítása a felhasználó böngészőjében<br>
A webböngészők (Google Chrome, Mozilla Firefox, Microsoft Edge stb) beállításai lehetőséget biztosítanak a sütik elfogadásának szabályozására, törlésére. Ezzel kapcsolatban az adott webböngésző útmutatásai az irányadóak. Ön a böngészési előzmények törlésével el tudja távolítani az összes cookie-t eszközéről. Ezzel a felkeresett weboldalakhoz tartozó összes cookie-t törölni tudja.
Ne feledje azonban, hogy az előzmények törlésével elveszíthet bizonyos elmentett információkat is (pl. bejelentkezési adatokat, keresési beállításokat).
