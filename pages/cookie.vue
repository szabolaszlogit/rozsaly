1.	A süti fogalma<br>
A süti (angolul cookie) egy kis információ csomag, amelyet a szerver küld a webböngészőnek, és a webböngésző minden szerver felé írányított kérés alkalmával visszaküld a webszervernek. A sütik a felhasználó eszközén tárolódnak.
Kérjük, tartsa szem előtt, hogy a sütik tiltása esetén egyes webes tartalmakhoz esetleg nem fér majd hozzá.<br><br>

