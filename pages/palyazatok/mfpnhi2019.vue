<p>Rozsály Község Önkormányzata sikeresen pályázott a Magyar Falu program keretében az MFP-NHI/2019 kódszámú pályázati kiírásra, A helyi kultúra segítése című pályázatával, melynek keretében 4 927 197 forint vissza nem térítendő támogatásban részesült.<br>
