Projekt megvalósításának kezdete: 2019.06.01 Projekt fizikai befejezése: 2020.05.31<br>
Eszközbeszerzés a helyi, térségi kulturális csoportok működtetéséhez, a közösségépítő rendezvények lebonyolításának megvalósításához. Néptánccsoport, népdalkör, gyermek és felnőtt színjátszó csoport, zenekar, gitároktatás és rendezvények használják majd a beszerzett eszközöket.<br>
A projekt tartalma: az eszköz és fellépő ruha beszerzésekkel a helyi, térségi művelődési, közösségépítési tevékenységet kívánjuk erősíteni, működtetni. Intézményünk számos programnak, tevékenységnek ad helyet, az eszközök beszerzésével ezek lebonyolítása, megvalósítása új lendületet kap. Terveink szerint a beszerzett néptánc fellépő ruhát a helyi amatőr néptánccsoport fogja használni, helyi és más fellépéseken.<br>
A beszerzett eszközök segítenek a helyi amatőr színjátszó csoport (felnőtt és gyermek), a helyi amatőr zenekar tevékenységében, műsoraik megvalósításában, valamint gitártanfolyamot tartunk gyermekek számára.
A projekt eredményei között az amatőr művészeti csoportok fejlődését, műsoraik színvonalának növekedését, rendezvények eredményesebb lebonyolítását várjuk. Ezáltal erősödik a helyiek kötődése a térséghez. Vonzó, értékes lehetőséget biztosítunk a helyi, térségi fiatalok és felnőttek számára, így a település megtartó ereje nő, közösségi kapcsolati fejlődnek.<br>
A projekt hatásaiként számítunk az önkéntes tevékenység megerősödésére, a kulturális közösségek fejlődésére.
A néptáncon keresztül pedig a magyar kulturális örökség megőrzését, megélését segítjük elő. Különböző korosztályok bevonásával (8- 60 éves kor) nő a generációk közötti kötelék.
A projekt indokoltsága az, hogy településünkön az önkormányzat, a művelődési ház keretein belül, vagy ezek segítségével zajlik szinte mindegyik kulturális, közösségépítő tevékenység, ezért az eszközbeszerzés nagyban elősegíti a helyi identitás megerősítését. Saját forrásból nem tudunk a helyi és nemzeti identitástudat erősítő tevékenységhez nagy értékű felszerelést vásárolni, viszont a programokra, nagy az igény, hiszen jelenleg is, és már hosszú ideje működtetünk kulturális csoportokat.</p>

